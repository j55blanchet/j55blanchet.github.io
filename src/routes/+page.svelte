    <h1 class="mb-2">
        Hi! I'm Julien
        <aside class="inline text-lg text-base-300">He/They</aside>
    </h1>
    
    <p>
        I'm a creative technologist. I'm currently in my 5th year pursuing a PhD Student in the <a href="https://rlab.cs.dartmouth.edu/home/">Robotics &amp; Reality Lab</a> at Dartmouth College, advised by <a href="https://rlab.cs.dartmouth.edu/devin/">Prof. Devin Balkcom</a>.         
    </p>
    <p>
        I've taken circuitous path getting to this point - since graduating from Dartmouth in 2015 with a computer science major and education minor, I've worked as an software engineer at a large tech company (Microsoft) and small farming-technology startup (<a href="https://news.mit.edu/2015/mit-startup-grove-labs-brings-urban-agriculture-indoors-1110">Grove Labs</a>), and then as a high school teacher (<a href="https://winchendon.org/">The Winchendon School</a>). In 2019, I returned to Dartmouth to pursue a PhD in Computer Science.
    </p>
    <p>
        My research interests lie primarily within the field of <strong>Human-Computer Interaction</strong>, incorporating elements of <strong>robotics</strong>, <strong>wearable devices</strong>, <strong>computer vision</strong>, and <strong>generative AI</strong>. Most of my current projects are focused on human motion teaching, flipping learning-by-demonstration on its head by using computers to teach us how to speak sign language, dance, and do yoga. 
        <!-- To accomplish this, we ... -->
    </p>
    <p>I'm passionate about bridging the gap between the quantitative, logical world of computers and the rich, messy, beautiful nature of our lived experience. In my work, I seek to use technology to help us connect more deeply with our bodies, our creative nature, that inner spark that makes us human - and then support us in sharing that with others. I envision a future where technology supports us in being the best, happiest versions of ourselves.
